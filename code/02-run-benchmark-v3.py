#!/usr/bin/env python3
"""
ClawBio BiB Benchmark v3 — 110 CPIC test cases × 9 models × 3 populations × 2 conditions × 3 runs.

Reads cases from ../SPECS/test_cases_v3.json (generated by 07-generate-cases-v3.py).

Per-model rate limiting addresses the v1 Mistral artefact: each provider gets a
configured inter-call interval based on its tier limits. Gemini and Mistral are
the most sensitive; reasoning models (o3, o4-mini) get slightly longer spacing
because their per-call latency is higher anyway.

Live cost tracker aborts the run if projected total exceeds BUDGET_HARD_CAP.

Outputs:
  ../RESULTS/v3_raw.json        — final result list
  ../RESULTS/v3_partial.json    — saved every 200 calls
  ../RESULTS/v3_checkpoint.json — counter, last (run, model, tc, pop, cond) processed
  ../RESULTS/v3_cost_log.json   — per-model token + cost tally
"""
