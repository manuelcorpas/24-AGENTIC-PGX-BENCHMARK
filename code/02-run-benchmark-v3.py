def cost_for(model: str, in_tok: int, out_tok: int) -> float:
    p_in, p_out = PRICE[model]
    return (in_tok * p_in + out_tok * p_out) / 1_000_000


def main(dry_run: bool = False, dry_run_models: list[str] | None = None,
         dry_run_n_cases: int | None = None, suffix: str = "",
         include_models: list[str] | None = None) -> None:
    paths = _output_paths(suffix)
    global OUT_RAW, OUT_PARTIAL, OUT_CKPT, OUT_COST
    OUT_RAW, OUT_PARTIAL, OUT_CKPT, OUT_COST = paths["raw"], paths["partial"], paths["ckpt"], paths["cost"]

    cases = json.loads(SPECS_FILE.read_text())
    if dry_run_n_cases:
        cases = cases[:dry_run_n_cases]
    models_to_run = MODELS
    if dry_run_models:
        models_to_run = {k: v for k, v in MODELS.items() if k in dry_run_models}
    elif include_models:
        models_to_run = {k: v for k, v in MODELS.items() if k in include_models}

    total = len(models_to_run) * len(cases) * len(POPULATIONS) * 2 * (1 if dry_run else N_RUNS)
    n_runs = 1 if dry_run else N_RUNS

    print(f"=== Benchmark v3 ===")
    print(f"Cases:       {len(cases)}")
    print(f"Models:      {list(models_to_run)}")
    print(f"Populations: {[p['id'] for p in POPULATIONS]}")
    print(f"Runs:        {n_runs}")
    print(f"Total calls: {total}")
    print(f"Budget cap:  ${BUDGET_HARD_CAP:.0f}\n")

    # Resume support
    results = []
    completed_keys = set()
    if OUT_PARTIAL.exists() and not dry_run:
        try:
            results = json.loads(OUT_PARTIAL.read_text())
            for r in results:
                completed_keys.add((r["run"], r["model"], r["tc"], r["pop"], r["cond"]))
            print(f"[resume] Loaded {len(results)} prior results from partial")
        except Exception:
            results = []

    cost_by_model = defaultdict(lambda: {"in_tok": 0, "out_tok": 0, "cost": 0.0, "calls": 0})
    if OUT_COST.exists() and not dry_run:
        try:
            saved = json.loads(OUT_COST.read_text())
            for k, v in saved.items():
                cost_by_model[k] = v
        except Exception:
            pass

    count = len(results)
    t0 = time.time()

    for run in range(n_runs):
        for mname, mfunc in models_to_run.items():
            spacing = SPACING.get(mname, 0.5)
            for tc in cases:
                for pop in POPULATIONS:
                    for cond in ["no_spec", "with_spec"]:
                        key = (run, mname, tc["id"], pop["id"], cond)
                        if key in completed_keys:
                            continue
                        count += 1
                        prompt = no_spec_prompt(tc, pop) if cond == "no_spec" else with_spec_prompt(tc, pop)

                        try:
                            time.sleep(spacing)
                            text, in_tok, out_tok = _retry(mfunc, prompt, max_retries=3, base_backoff=2.0)
                            parsed_resp = parse(text)
                            scores = score(parsed_resp, tc, pop)
                            entry = {"run": run, "model": mname, "tc": tc["id"],
                                     "gene": tc["gene"], "drug": tc.get("drug", ""), "pop": pop["id"],
                                     "cond": cond, "parsed": parsed_resp, "scores": scores,
                                     "in_tok": in_tok, "out_tok": out_tok}
                            cb = cost_by_model[mname]
                            cb["in_tok"] += in_tok
                            cb["out_tok"] += out_tok
                            cb["cost"] += cost_for(mname, in_tok, out_tok)
                            cb["calls"] += 1
                        except Exception as e:
                            entry = {"run": run, "model": mname, "tc": tc["id"],
                                     "gene": tc["gene"], "drug": tc.get("drug", ""), "pop": pop["id"],
                                     "cond": cond, "parsed": {},
                                     "scores": {"A1": 0, "A2": 0, "A3": 0, "B1": 0, "B2": 0, "B3": 0,
                                                "tier_a": 0, "tier_b": 0, "overall": 0, "format_fail": True},
                                     "error": str(e)[:200],
                                     "in_tok": 0, "out_tok": 0}
                            print(f"  ERROR [{count}/{total}] {mname} | {tc['id']} | {pop['id']} | {cond}: {str(e)[:120]}")

                        results.append(entry)

                        # Progress + budget check + checkpointing
                        if count % 25 == 0 or count == total:
                            total_cost = sum(v["cost"] for v in cost_by_model.values())
                            elapsed = time.time() - t0
                            rate = count / elapsed if elapsed > 0 else 0
                            eta_s = (total - count) / rate if rate > 0 else 0
                            print(f"  [{count}/{total}] {mname} | {tc['id'][:25]:<25} | {pop['id']} | {cond:<9} | "
                                  f"cost ${total_cost:.2f} | rate {rate:.1f}/s | ETA {eta_s/60:.0f} min")
                            if total_cost > BUDGET_HARD_CAP:
                                print(f"\n!!! BUDGET CAP EXCEEDED (${total_cost:.2f} > ${BUDGET_HARD_CAP:.0f}) — saving and aborting")
                                _save_all(results, cost_by_model, count, total)
                                return
                            if total_cost > BUDGET_WARN:
                                print(f"  >> warning: cost ${total_cost:.2f} above warn threshold ${BUDGET_WARN:.0f}")
                        if count % 100 == 0 and not dry_run:
                            _save_all(results, cost_by_model, count, total)

    _save_all(results, cost_by_model, count, total)
    print(f"\n=== Run complete: {count} calls ===")
    summary = {"calls": count, "cost_total": sum(v["cost"] for v in cost_by_model.values()),
               "by_model": dict(cost_by_model)}
    print(json.dumps(summary, indent=2, default=str))


def _save_all(results, cost_by_model, count, total):
    final = OUT_RAW if count >= total else OUT_PARTIAL
    final.write_text(json.dumps(results, indent=2))
    OUT_PARTIAL.write_text(json.dumps(results, indent=2))
    OUT_CKPT.write_text(json.dumps({"count": count, "total": total}))
    OUT_COST.write_text(json.dumps({k: dict(v) for k, v in cost_by_model.items()}, indent=2, default=str))


