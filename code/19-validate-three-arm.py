#!/usr/bin/env python3
"""
Three-arm validation gate — methodology artefact for the cpic_rag extension.

THIS IS NOT THE CANONICAL VALIDATION GATE. The canonical gate for the two-arm
v3 dataset (no_spec + with_spec only) remains 09-validate-v3.py, which backs
the v3 numbers in the manuscript and the v6 Supporting Information audit
trail. Modifying that file would invalidate the existing audit trail.

This script is a parallel three-arm-aware gate for v3_raw_rescored_three_arm.json
(the merged dataset produced by 17-merge-rag-results.py). It accepts datasets
with 1, 2, or 3 conditions present and adapts R4 (cell completeness), R5/R6/R7
(per-condition reports), and R10 (per-condition ceiling) accordingly.

R10 semantics:
  - with_spec ceiling is asserted (must be 100% A1/A2/A3) — the manuscript's
    central claim.
  - cpic_rag ceiling is REPORTED but NOT ASSERTED — its expected value is the
    open empirical question this experiment exists to answer.
  - no_spec ceiling is reported only.

Usage:
  python3 19-validate-three-arm.py [path-to-rescored-json]

Default path: ../RESULTS/v3_raw_rescored_three_arm.json (post-merge)
Pre-flight: pass ../RESULTS/v3_raw_rescored.json to verify the gate passes
            on the existing two-arm dataset (no cpic_rag rows present).

Reads:
  <input json>
  ../SPECS/test_cases_v3.json
Writes:
  ../RESULTS/v3_three_arm_validation_report.txt
"""
from __future__ import annotations
import json
import re
import sys
from collections import Counter, defaultdict
from importlib.util import spec_from_file_location, module_from_spec
from pathlib import Path

BASE = Path(__file__).resolve().parent.parent
PYDIR = BASE / "PYTHON"
DEFAULT_RAW = BASE / "RESULTS" / "v3_raw_rescored_three_arm.json"
PREFLIGHT_RAW = BASE / "RESULTS" / "v3_raw_rescored.json"
CASES_FILE = BASE / "SPECS" / "test_cases_v3.json"
REPORT = BASE / "RESULTS" / "v3_three_arm_validation_report.txt"

_spec = spec_from_file_location("rescore", PYDIR / "10-rescore-v3.py")
rescore = module_from_spec(_spec); _spec.loader.exec_module(rescore)

VALID_CONDS = ("no_spec", "cpic_rag", "with_spec")


REGRESSION_CASES = [
    ("Poor Metaboliser", "Poor Metaboliser", 1.0),
    ("Ultra-rapid Metaboliser", "Ultra-rapid Metaboliser", 1.0),
    ("Favourable Response", "favorable response", 1.0),
    ("Favourable Response", "Favourable Response", 1.0),
    ("Variant Carrier", "Variant Carrier", 1.0),
    ("Normal Metaboliser (Expressor)", "Normal Metaboliser", 1.0),
    ("Non-responsive", "Non-responsive", 1.0),
    ("Normal", "Normal", 1.0),
    ("Normal", "Normal Metaboliser", 0.0),
    ("Variable", "g6pd deficient (variable activity due to x-inactivation)", 0.5),
    ("Poor Metaboliser", "intermediate metabolizer (if *1/*4) or poor metabolizer (if *4/*4)", 0.5),
    ("Poor Metaboliser", "Normal Metaboliser", 0.0),
    ("Ultra-rapid Metaboliser", "Normal Metaboliser", 0.0),
]

REGRESSION_A2 = [
    ("codeine: AVOID (potentially LETHAL morphine accumulation)", "AVOID codeine", 1.0),
    ("warfarin: use validated pharmacogenetic dosing algorithm", "warfarin: use validated pharmacogenetic dosing algorithm", 1.0),
    ("ivacaftor: not indicated (use combination therapy with elexacaftor/tezacaftor)",
     "ivacaftor: not indicated (use combination therapy with elexacaftor/tezacaftor)", 1.0),
    ("ivacaftor: indicated; gating mutation responsive",
     "ivacaftor: indicated; gating mutation responsive", 1.0),
    ("volatile-anaesthetics: standard use", "volatile-anaesthetics: standard use", 1.0),
    ("fluorouracil: AVOID (potentially LETHAL)", "fluorouracil: standard dosing", 0.0),
    ("warfarin: standard dosing", "warfarin: AVOID", 0.0),
]


def section(title: str) -> str:
    return f"\n{'=' * 70}\n{title}\n{'=' * 70}"


def main():
    if len(sys.argv) > 1:
        raw_path = Path(sys.argv[1])
    elif DEFAULT_RAW.exists():
        raw_path = DEFAULT_RAW
    else:
        raw_path = PREFLIGHT_RAW
        print(f"[pre-flight] {DEFAULT_RAW.name} not found; validating against existing two-arm {raw_path.name}")

    rows = json.loads(raw_path.read_text())
    cases = json.loads(CASES_FILE.read_text())
    cbi = {c["id"]: c for c in cases}

    out = [
        "v3 THREE-ARM VALIDATION REPORT",
        f"Source: {raw_path.name}",
        f"Rows: {len(rows)} | Cases: {len(cases)}",
        f"Generated by: 19-validate-three-arm.py",
        "",
        "Note: 09-validate-v3.py remains the canonical gate for the two-arm",
        "v3 dataset that backs the manuscript's headline numbers. This script",
        "is the parallel gate for the three-arm cpic_rag extension and accepts",
        "datasets with 1, 2, or 3 conditions present.",
    ]
    failures = []

    # ---- detect which conditions are present ----
    conds_present = sorted({r["cond"] for r in rows}, key=lambda c: VALID_CONDS.index(c) if c in VALID_CONDS else 99)
    unknown_conds = [c for c in conds_present if c not in VALID_CONDS]
    out.append(section("Conditions in this dataset"))
    out.append(f"  present: {conds_present}")
    if unknown_conds:
        failures.append(f"Unknown conditions: {unknown_conds}")

    # ---- R1: classifier regression ----
    out.append(section("R1: Classifier regression tests"))
    for gt, parsed, expected in REGRESSION_CASES:
        actual = rescore.score_a1(parsed, gt)
        status = "PASS" if abs(actual - expected) < 0.01 else "FAIL"
        if status == "FAIL":
            failures.append(f"R1 A1: gt={gt!r} parsed={parsed!r} expected={expected} actual={actual}")
        out.append(f"  [{status}] A1({gt!r}, {parsed!r}) = {actual} (expected {expected})")
    for gt, parsed, expected in REGRESSION_A2:
        actual = rescore.score_a2(parsed, gt)
        status = "PASS" if abs(actual - expected) < 0.01 else "FAIL"
        if status == "FAIL":
            failures.append(f"R1 A2: gt={gt!r} parsed={parsed!r} expected={expected} actual={actual}")
        out.append(f"  [{status}] A2({gt!r}, {parsed!r}) = {actual} (expected {expected})")

    # ---- R2: gt-phenotype canonical-tier mapping ----
    out.append(section("R2: gt_phenotype canonical-tier mapping"))
    unique_phens = sorted({c["gt_phenotype"] for c in cases})
    for p in unique_phens:
        t = rescore.gt_tier(p)
        det = rescore.detected_tiers(p)
        status = "PASS" if len(det) == 1 else "WARN"
        if status == "WARN":
            failures.append(f"R2: gt_phenotype={p!r} detected multiple tiers: {det}")
        out.append(f"  [{status}] {p!r:<45} -> {t}  (all_tiers: {det})")

    # ---- R3: substring false-positive screen (no_spec only — only condition where the old scorer ever applied) ----
    out.append(section("R3: A1 substring false-positive screen (no_spec)"))
    fp = 0
    fp_examples = []
    for r in rows:
        if r["scores"].get("format_fail"): continue
        if r["scores"].get("A1") != 1.0: continue
        if r["cond"] != "no_spec": continue
        parsed_phen = (r["parsed"] or {}).get("PHENOTYPE", "")
        gt_phen = cbi[r["tc"]]["gt_phenotype"]
        if rescore.score_a1(parsed_phen, gt_phen) != 1.0:
            fp += 1
            if len(fp_examples) < 5:
                fp_examples.append((r["model"], r["tc"], r["pop"], gt_phen, parsed_phen))
    status = "PASS" if fp == 0 else "FAIL"
    out.append(f"  [{status}] {fp} no_spec rows scored A1=1.0 by old scorer but rejected by classifier")
    if fp > 0:
        failures.append(f"R3: {fp} substring false positives still detected")

    # ---- R4: output completeness, three-arm-aware ----
    out.append(section("R4: Output completeness (three-arm-aware)"))
    n_runs = max(r["run"] for r in rows) + 1
    models = sorted({r["model"] for r in rows})
    pops = sorted({r["pop"] for r in rows})
    n_conds = len(conds_present)
    expected_cells = n_runs * len(models) * len(cases) * len(pops) * n_conds
    n_cells = len(rows)
    out.append(f"  expected: {expected_cells} cells = {n_runs} runs × {len(models)} models × {len(cases)} cases × {len(pops)} pops × {n_conds} conds ({conds_present})")
    out.append(f"  actual:   {n_cells} cells")
    status = "PASS" if n_cells == expected_cells else "FAIL"
    out.append(f"  [{status}]")
    if n_cells != expected_cells:
        failures.append(f"R4: expected {expected_cells} cells, got {n_cells}")

    # ---- R5: parse failures by condition × model ----
    out.append(section("R5: Parse-failure concentration"))
    for cond in conds_present:
        out.append(f"  {cond}:")
        for model in models:
            n = sum(1 for r in rows if r["cond"] == cond and r["model"] == model)
            pf = sum(1 for r in rows if r["cond"] == cond and r["model"] == model and r["scores"].get("format_fail"))
            out.append(f"    {model:<22} {pf}/{n} parse_fail")

    # ---- R6: 3-run consistency by condition ----
    out.append(section("R6: 3-run consistency (perfect 3/3 A1 rate)"))
    for cond in conds_present:
        by_combo = defaultdict(list)
        for r in rows:
            if r["cond"] == cond and not r["scores"].get("format_fail"):
                by_combo[(r["model"], r["tc"], r["pop"])].append(r)
        eligible = {k: v for k, v in by_combo.items() if len(v) >= 2}
        perfect = sum(1 for v in eligible.values() if all(x["scores"]["A1"] == 1.0 for x in v))
        pct = 100 * perfect / len(eligible) if eligible else 0
        out.append(f"  {cond}: {perfect}/{len(eligible)} ({pct:.1f}%) perfect")

    # Hard assertion: with_spec must achieve 100% consistency
    if "with_spec" in conds_present:
        by_combo = defaultdict(list)
        for r in rows:
            if r["cond"] == "with_spec" and not r["scores"].get("format_fail"):
                by_combo[(r["model"], r["tc"], r["pop"])].append(r)
        eligible_ws = {k: v for k, v in by_combo.items() if len(v) >= 2}
        perfect_ws = sum(1 for v in eligible_ws.values() if all(x["scores"]["A1"] == 1.0 for x in v))
        if eligible_ws and perfect_ws != len(eligible_ws):
            failures.append(f"R6: with_spec consistency {perfect_ws}/{len(eligible_ws)} not 100%")

    # ---- R7: aggregate accuracy ----
    out.append(section("R7: Aggregate accuracy (parsed only)"))
    parsed_only = [r for r in rows if not r["scores"].get("format_fail")]
    for cond in conds_present:
        rs = [r for r in parsed_only if r["cond"] == cond]
        out.append(f"  {cond} (n={len(rs)}):")
        for dim in ["A1", "A2", "A3"]:
            correct = sum(1 for r in rs if r["scores"].get(dim) == 1.0)
            partial = sum(1 for r in rs if r["scores"].get(dim) == 0.5)
            out.append(f"    {dim}: {correct}/{len(rs)} ({100*correct/len(rs):.2f}%) full + {partial} partial")

    # Sanity bounds:
    #   - with_spec >= no_spec on A1, A2 (the spec is supposed to help, not hurt)
    #   - cpic_rag should land somewhere between no_spec and with_spec on A1
    if "no_spec" in conds_present and "with_spec" in conds_present:
        for dim in ["A1", "A2"]:
            ns = [r for r in parsed_only if r["cond"] == "no_spec"]
            ws = [r for r in parsed_only if r["cond"] == "with_spec"]
            ns_pct = sum(1 for r in ns if r["scores"].get(dim) == 1.0) / len(ns) if ns else 0
            ws_pct = sum(1 for r in ws if r["scores"].get(dim) == 1.0) / len(ws) if ws else 0
            if ws_pct < ns_pct:
                failures.append(f"R7: with_spec {dim}={ws_pct:.3f} unexpectedly < no_spec {dim}={ns_pct:.3f}")

    # ---- R8: DPYD lethal case ----
    out.append(section("R8: DPYD/fluorouracil PM (lethal case)"))
    for cond in conds_present:
        rs = [r for r in rows if r["tc"] == "dpyd_fu_pm" and r["cond"] == cond and not r["scores"].get("format_fail")]
        errs = [r for r in rs if r["scores"].get("A3", 1.0) < 1.0]
        out.append(f"  {cond}: {len(errs)} A3 errors / {len(rs)} parsed")
        for pop in ["EUR", "AMR", "AFR"]:
            ps = [r for r in rs if r["pop"] == pop]
            pe = [r for r in ps if r["scores"].get("A3", 1.0) < 1.0]
            out.append(f"    {pop}: {len(pe)}/{len(ps)} errors")
    out.append("  Lethal-case A3 errors per gene (no_spec, parsed):")
    by_gene_ns = Counter()
    for r in rows:
        if r["cond"] != "no_spec" or r["scores"].get("format_fail"): continue
        c = cbi[r["tc"]]
        if "lethal" not in c["gt_drug"].lower(): continue
        if r["scores"].get("A3", 1.0) < 1.0:
            by_gene_ns[c["gene"]] += 1
    for gene, n in by_gene_ns.most_common():
        out.append(f"    {gene:<20} {n}")

    # ---- R9: Tier B drift ----
    out.append(section("R9: Tier B (B1, B2, B3) drift"))
    for cond in conds_present:
        rs = [r for r in parsed_only if r["cond"] == cond]
        n = len(rs)
        out.append(f"  {cond} (n={n}):")
        for dim in ["B1", "B2", "B3"]:
            avg = sum(r["scores"].get(dim, 0) for r in rs) / n if n else 0
            out.append(f"    {dim} mean: {avg:.3f}")

    # ---- R10: per-condition ceiling ----
    out.append(section("R10: Per-condition ceiling"))
    for cond in conds_present:
        rs = [r for r in parsed_only if r["cond"] == cond]
        if not rs:
            continue
        line = f"  {cond} (n={len(rs)}):"
        out.append(line)
        for dim in ["A1", "A2", "A3"]:
            correct = sum(1 for r in rs if r["scores"].get(dim) == 1.0)
            pct = 100 * correct / len(rs)
            if cond == "with_spec":
                # asserted: must hit 100%
                status = "PASS" if correct == len(rs) else "FAIL"
                out.append(f"    [{status} ASSERT] {dim} {cond}: {correct}/{len(rs)} = {pct:.2f}% (must be 100% for with_spec)")
                if correct != len(rs):
                    failures.append(f"R10: with_spec {dim} = {correct}/{len(rs)} not 100%")
            elif cond == "cpic_rag":
                # reported: empirical question of this experiment
                out.append(f"    [REPORT  ] {dim} {cond}: {correct}/{len(rs)} = {pct:.2f}% (cpic_rag ceiling is the experimental output, not asserted)")
            else:
                out.append(f"    [REPORT  ] {dim} {cond}: {correct}/{len(rs)} = {pct:.2f}%")

    # ---- final ----
    out.append(section("OVERALL"))
    if failures:
        out.append(f"FAILED ({len(failures)} issues)")
        for f in failures:
            out.append(f"  - {f}")
    else:
        out.append("PASS — all asserted checks passed; cpic_rag ceiling is reported only.")

    text = "\n".join(out)
    REPORT.write_text(text)
    print(text)
    print(f"\nReport saved to {REPORT}")
    sys.exit(0 if not failures else 1)


if __name__ == "__main__":
    main()
